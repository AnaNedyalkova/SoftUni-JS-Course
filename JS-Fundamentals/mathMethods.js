// 1. Math.abs 
function difference(a, b) {
    return Math.abs(a - b);
  }
  console.log(difference(3, 5)); //=> 2

// 2. Math.trunc 
// returns the integer part of a number by removing any fractional
console.log(Math.trunc(13.37)); //=> 13

// 3. Math.ceil
console.log(Math.ceil(0.95)) // => 1

// 4. Math.floor
console.log(Math.floor(5.95)); // => 5

// 5. Math.max
console.log(Math.max(1, 3, 2)); // => 3
// 6. Math.min
console.log(Math.min(2, 3, 1)); // => 1

// 7. Math.pow == **  
// syntax: Math.pow(base, exponent)
Math.pow(7, 2); // => 49

// 8. Math.round 
//static method returns the value of a number rounded to the nearest integer.
console.log(Math.round(5.95), Math.round(5.5), Math.round(5.05)); // => 6 6 5

